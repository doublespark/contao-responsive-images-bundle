namespace Doublespark\ContaoResponsiveImagesBundle\ContaoManager;

use Contao\CoreBundle\ContaoCoreBundle;
use Contao\ManagerPlugin\Bundle\BundlePluginInterface;
use Contao\ManagerPlugin\Bundle\Config\BundleConfig;
use Contao\ManagerPlugin\Bundle\Parser\ParserInterface;
use Doublespark\ContaoResponsiveImagesBundle\DoublesparkContaoResponsiveImagesBundle;

class Plugin implements BundlePluginInterface
{
    /**
     * {@inheritdoc}
     */
    public function getBundles(ParserInterface $parser): array
    {
        return [
            BundleConfig::create(DoublesparkContaoResponsiveImagesBundle::class)->setLoadAfter([ContaoCoreBundle::class])
        ];
    }
}
